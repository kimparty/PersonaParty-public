export function createTranslator(locale = 'zh-CN') {
  const table = messages[locale] ?? messages['zh-CN'];
  return function translate(key, ...args) {
    const value = table[key] ?? messages['zh-CN'][key] ?? key;
    return typeof value === 'function' ? value(...args) : value;
  };
}
